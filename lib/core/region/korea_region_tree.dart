List<String> districtsForSido(String sido) {
  if (sido == '서울') {
    return <String>['전체', ..._seoulGu];
  }
  return List<String>.from(_otherSidoDistricts[sido] ?? const <String>['전체']);
}

List<String> dongsFor(String sido, String district) {
  if (district == '전체') {
    return const <String>['전체'];
  }
  if (sido == '서울') {
    return List<String>.from(kSeoulGuDongs[district] ?? const <String>['전체']);
  }
  final byDistrict = kNonSeoulDistrictDongs[sido];
  if (byDistrict != null) {
    final list = byDistrict[district];
    if (list != null && list.isNotEmpty) {
      return List<String>.from(list);
    }
  }
  return const <String>['전체'];
}

/// 공백으로 이어 붙인 저장용 경로. 예: `서울 강남구 개포2동`, `서울`, `경기 수원시 장안구`
String buildRegionStoragePath({
  required String sido,
  required String district,
  required String dong,
}) {
  if (district == '전체') {
    return sido;
  }
  if (dong == '전체') {
    return '$sido $district';
  }
  return '$sido $district $dong';
}

String regionPathDisplayChevron(String storagePath) {
  final parts = storagePath.trim().split(RegExp(r'\s+'));
  if (parts.isEmpty) return '';
  return parts.join(' > ');
}

bool regionPathEquals(String a, String b) =>
    a.trim().replaceAll(RegExp(r'\s+'), ' ') ==
    b.trim().replaceAll(RegExp(r'\s+'), ' ');
